int run(int N, int K, VI s)
{
  // suposed i people are the targets
  for(int i = 0; i < N; i++){
    // i people buy over K items
    int left = 0;
    for(int p: s){
      if(p >= i)
        left += p - i;
    }
    if ((K-1)*(N-i) >= left)
      return i;
  }
  return 0;
}

